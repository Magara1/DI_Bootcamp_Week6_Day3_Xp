package xp;

public class Print_stars {

	public static void main(String[] args) {
    
		int n = 5;
	    int i, j;
	 
	   
	    for (i = 0; i<= n; i++)
	    	
	    {
	        for (j = 2; j <= i; j++) {
	            System.out.print("*");
	        }
	 
	        System.out.print("\n");
	    }
	  
	  }
	
	}


